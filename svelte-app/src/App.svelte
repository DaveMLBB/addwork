<script>
	export let name;

    let message = 'Loading...';

    async function fetchMessage() {
        const res = await fetch('http://localhost:8080');
        message = await res.text();
    }

    fetchMessage();
</script>

<main>
	<h1>Hello {name}!</h1>
	<p>Visit the <a href="https://svelte.dev/tutorial">Svelte tutorial</a> to learn how to build Svelte apps.</p>
    <h1>{message}</h1>
</main>


